#!/usr/bin/env node
// Generates frontend/src/spriteData.ts from per-sprite JSON files.
//
//   node tools/pixel-art/build-sprite-data.mjs <json-dir>
//
// Each JSON: { name, scale?, frameRate?, palette: string[], frames: number[][][] }
// palette[0] must be "transparent". Validates every grid cell against the
// palette before emitting. Reusable for any game that adopts the SpriteDef
// format (see frontend/src/pixel/sprite.ts).
import fs from 'node:fs';
import path from 'node:path';

const jsonDir = process.argv[2];
if (!jsonDir) {
  console.error('usage: build-sprite-data.mjs <json-dir>');
  process.exit(1);
}
const outFile = path.join(
  path.dirname(new URL(import.meta.url).pathname),
  '..', '..', 'frontend', 'src', 'spriteData.ts'
);

const files = fs.readdirSync(jsonDir).filter((f) => f.endsWith('.json')).sort();
const defs = [];
const errors = [];

for (const file of files) {
  const raw = JSON.parse(fs.readFileSync(path.join(jsonDir, file), 'utf8'));
  const name = raw.name ?? path.basename(file, '.json');
  const { palette, frames } = raw;
  if (!Array.isArray(palette) || palette[0] !== 'transparent') {
    errors.push(`${file}: palette[0] must be "transparent"`);
    continue;
  }
  if (!Array.isArray(frames) || frames.length === 0) {
    errors.push(`${file}: no frames`);
    continue;
  }
  let ok = true;
  for (const [fi, grid] of frames.entries()) {
    const width = grid[0]?.length ?? 0;
    for (const [ri, row] of grid.entries()) {
      if (row.length !== width) {
        errors.push(`${file}: frame ${fi} row ${ri} ragged (${row.length} vs ${width})`);
        ok = false;
      }
      for (const cell of row) {
        if (!Number.isInteger(cell) || cell < 0 || cell >= palette.length) {
          errors.push(`${file}: frame ${fi} row ${ri} has out-of-palette index ${cell}`);
          ok = false;
          break;
        }
      }
    }
  }
  if (!ok) continue;
  defs.push({ name, scale: raw.scale ?? 8, frameRate: raw.frameRate ?? 6, palette, frames });
}

if (errors.length) {
  console.error('VALIDATION ERRORS:\n' + errors.join('\n'));
  process.exit(1);
}

const gridToTs = (grid) =>
  '[\n' + grid.map((row) => `        [${row.join(',')}],`).join('\n') + '\n      ]';

const entries = defs.map((d) => {
  const frames = d.frames.map(gridToTs).join(', ');
  const palette = d.palette.map((c) => `'${c}'`).join(', ');
  return `  ${JSON.stringify(d.name)}: {
    scale: ${d.scale},
    frameRate: ${d.frameRate},
    palette: [${palette}],
    frames: [${frames}],
  },`;
}).join('\n');

fs.writeFileSync(outFile, `/**
 * Enemy sprite definitions, data-only. GENERATED by
 * tools/pixel-art/build-sprite-data.mjs — edit the source JSONs and re-run
 * rather than hand-editing. Art rules: tools/pixel-art/SPRITE-STYLE.md.
 *
 * Entries here override any legacy hand-coded sprite of the same name in
 * sprites.ts, so sprites can be migrated to data one at a time.
 */

import type { SpriteDef } from './pixel/sprite';

export const ENEMY_SPRITE_DATA: Record<string, SpriteDef> = {
${entries}
};
`);
console.log(`wrote ${defs.length} sprites → ${outFile}`);

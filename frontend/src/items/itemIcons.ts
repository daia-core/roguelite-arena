const ART_SCALE = 6; // canvas px per art pixel

const cache = new Map<string, HTMLCanvasElement>();

function renderGrid(g: number[][], p: string[]): HTMLCanvasElement {
  const rows = g.length;
  const cols = Math.max(...g.map((r) => r.length));
  const canvas = document.createElement('canvas');
  canvas.width = cols * ART_SCALE;
  canvas.height = rows * ART_SCALE;
  const ctx = canvas.getContext('2d')!;
  ctx.imageSmoothingEnabled = false;
  for (let y = 0; y < rows; y++) {
    const row = g[y];
    for (let x = 0; x < row.length; x++) {
      const c = row[x];
      if (c > 0 && p[c] && p[c] !== 'transparent') {
        ctx.fillStyle = p[c];
        ctx.fillRect(x * ART_SCALE, y * ART_SCALE, ART_SCALE, ART_SCALE);
      }
    }
  }
  return canvas;
}

// Deterministic 32-bit hash of a string (codepoint-aware).
function hash(str: string): number {
  let h = 2166136261;
  for (const ch of str) {
    h ^= ch.codePointAt(0)!;
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}

// Procedural fallback: a unique, left-right-symmetric pixel rune whose shape and
// palette are seeded from the emoji, so every un-mapped item still gets a
// distinct pixel icon (never a text emoji).
function runeFor(emoji: string): HTMLCanvasElement {
  const seed = hash(emoji);
  const hue = seed % 360;
  const p = [
    'transparent',
    `hsl(${hue} 60% 22%)`, // outline
    `hsl(${hue} 70% 48%)`, // fill
    `hsl(${hue} 85% 72%)`, // highlight
  ];
  const N = 11;
  const half = Math.ceil(N / 2);
  const g: number[][] = Array.from({ length: N }, () => new Array(N).fill(0));
  let s = seed || 1;
  const rnd = () => {
    // xorshift
    s ^= s << 13; s ^= s >>> 17; s ^= s << 5; s >>>= 0;
    return s / 0xffffffff;
  };
  // Grow a blobby symmetric shape from the centre column outward.
  for (let y = 1; y < N - 1; y++) {
    for (let x = 0; x < half; x++) {
      const distC = Math.abs(y - (N - 1) / 2) / N + Math.abs(x - half) / N;
      const on = rnd() > 0.32 + distC;
      if (on) {
        g[y][x] = 2;
        g[y][N - 1 - x] = 2;
      }
    }
  }
  // Highlight + outline pass.
  for (let y = 0; y < N; y++) {
    for (let x = 0; x < N; x++) {
      if (g[y][x] === 2) {
        // highlight the upper-left interior
        if (y > 0 && x > 0 && g[y - 1][x] === 2 && g[y][x - 1] === 2 && rnd() > 0.6) {
          g[y][x] = 3;
        }
        // outline any fill pixel that borders empty space
        const nb = [[0,1],[0,-1],[1,0],[-1,0]];
        for (const [dy, dx] of nb) {
          const ny = y + dy, nx = x + dx;
          if (ny < 0 || nx < 0 || ny >= N || nx >= N || g[ny][nx] === 0) {
            // mark border as outline unless it's already a fill neighbour we keep
            if (g[ny] && g[ny][nx] === 0) g[ny][nx] = -1;
          }
        }
      }
    }
  }
  for (let y = 0; y < N; y++) {
    for (let x = 0; x < N; x++) {
      if (g[y][x] === -1) g[y][x] = 1;
    }
  }
  return renderGrid(g, p);
}

/** Get (and cache) the pixel-art icon canvas for an item's emoji glyph. */
export function getItemIcon(emoji: string): HTMLCanvasElement {
  const key = emoji || '?';
  const cached = cache.get(key);
  if (cached) return cached;
  const glyphName = EMOJI_MAP[emoji];
  const canvas = glyphName
    ? renderGrid(GLYPHS[glyphName].g, GLYPHS[glyphName].p)
    : runeFor(key);
  cache.set(key, canvas);
  return canvas;
}

/** True when we have a hand-authored glyph (vs a procedural rune) — for QA/coverage. */
export function hasAuthoredGlyph(emoji: string): boolean {
  return emoji in EMOJI_MAP;
}
